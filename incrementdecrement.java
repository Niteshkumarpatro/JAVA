public class incrementdecrement {
    public static void main(String[] args) {
         int x=4;
        System.out.println(x);
         x++;
        System.out.println(x);
         x--;
        System.out.println(x); 
        
        System.out.println(++x);
        
         System.out.println(--x);
         // x++ is "use x and then increment" this is post incrrement operator
         //  ++x is "increment x and then use " this is pre increment operator




        




        
    }
    
}
